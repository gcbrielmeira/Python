# Conversor de Temperatura

"""
Solicite ao Usuario para fornecer uma temperatura em Graus
Converta essa temperatura em Fahrenheit
"""

print("Qual a temperatura da sua cidade em GRAUS CELSIUS ? ")
celcious = float(input())

Fahrenheit = (celcious * 1.8) + 32

print("essa e a temperatura em fahrenheit: F", Fahrenheit)
